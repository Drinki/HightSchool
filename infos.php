						Les écoles de commerce sont des écoles privées enseignant la gestion et le commerce. Les études peuvent aller du bac+3 au bac+5. Ces écoles recrutent sur dossier directement après le bac ou après une école préparatoire aux grandes écoles. Majoritairement, les élèves des écoles de commerce sont des bacheliers ES, viennent ensuite les bacheliers S et dans une minorité, les bacheliers L et STMG.
						Après une école de commerce, beaucoup de métier sont accessible : la vente, la finance, la gestion, l'information, le marketing, les ressources humaines.
					</td>
					<td class="columns">
						Les écoles préparatoires aux grandes écoles sont des formations sur deux ans accessibles après le bac. Se sont des formations réparties en quatre filières : scientifiques, économiques, littéraires ou artistiques. Le but de ce cursus est de pouvoir ensuite intégrer des écoles de commerce, d'ingénieurs, mais aussi des écoles de vétérinaires ou militaires, science-politique...
						Les écoles préparatoires aux grandes écoles ne donnent pas accès directement au monde professionnel, mais c'est un moyen d'être préparé à de longues études.
					</td>
					<td class="columns">
						Les écoles paramédicales préparent les étudiants durant 1 à 3 ans aux diplômes obligatoires pour exercer les métiers d’infirmiers, d’audioprothésistes, d’ergothérapeutes, de manipulateurs en électroradiologie médicale. La plupart des étudiants suivant ces formations proviennent du bac ST2S après le passage par un concours. Mais quelques-uns de ces concours sont accessibles sans le bac. Cette formation vous ouvre les portes dans le domaine du soin, de la rééducation, de l'appareillage, de l'alimentation et du bien-être et dans l'assistance médico-technique...
					</td>
					<td class="columns">
						Suivant le diplôme envisagé, les études dans cette formation ont une durée allant de 1 à 3 ans. L'accès aux écoles du social est fait sur concours ou sur dossier qui est ensuite suivi d'un entretien visant à évaluer la motivation et l'aptitude pour le métier. Les concours sont accessibles avec ou sans le bac.
						Cette formation ouvre les portes sur des métiers comme éducateur spécialisé, éducateur de jeunes enfants, moniteur-éducateur, assistant de service-sociale, conseiller en économie sociale et familiale.
