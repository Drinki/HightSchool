					<td class="columns">
						Une licence est une formation universitaire qui est divisée en six semestres. Cette formation nécessite une implication personnelle importante, car le nombre d'heures de cours est moindre comparé à d'autre formation. Les licences recouvrent un large panel de formation comme : le droit, l'économie, les sciences, les langues, les arts...
						Le choix d'une licence inclut majoritairement des études longues avec une continuité sur un master (bac+5), allant jusqu'au doctorat (bac+8). Mais également des débouchés vers des écoles de commerce, d'ingénieur ou encore des licences professionnelles...
					</td>
					<td class="columns">
						Le DUT (Diplôme Universitaire et Technologique) est une formation divisé en 4 semestres qui se déroule au sein d'un IUT (Institut Universitaire de Technologie) . Cette formation propose les avantages de l’université avec un encadrement plus poussé. Il permet de se professionnaliser avec des stages obligatoires en entreprises pour valider le diplôme.
						Après un BTS, on note deux débouchés :  
						<ul><li>-L'insertion professionnelle, avec une entrée immédiate après la formation dans le monde du travail.</li>
						<li>-La poursuite des études, avec une entrée sous dossier dans des licences professionnelle, des écoles d'ingénieurs ou encore de commerce...</li></ul>
					</td>
					<td class="columns">
						Le BTS (Brevet Technologique Supérieur) est une formation qui se prépare en 4 semestres après le bac. Les diplômes délivrés correspondent à un domaine professionnel précis et répondant aux exigences des recruteurs et des entreprises. C'est une formation avec un accès sélectif, où les élèves sont encadrés et le rythme de travail est pour le moins soutenu.
						La minorité des élèves suivant un BTS continu, leurs études ultérieurement. Ainsi, la plupart des débouchés après un BTS est un accès immédiat à la vie professionnelle. Selon le cursus suivi, le marché du travail sera plus ou moins ouvert à l'embauche.
					</td>
					<td class="columns">
						Intégrée une école d'ingénieurs implique une formation de cinq ans dans des domaines scientifiques et de gestion de projets. L’accès à une école d'ingénieurs nécessite un passage en classe préparatoire aux grandes écoles, après un BTS ou DUT, mais également directement après le bac sur concours.
						Après une formation d'ingénieur, la voie professionnelle est ouverte. Toutes les entreprises recrutent des ingénieurs. Cette formation offre touts les acquis pour effectuer une carrière satisfaisante. Tout en Sachant que de nombreuses écoles sont en partenariats directs avec des entreprises.
